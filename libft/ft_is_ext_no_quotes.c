#include "libft.h"

int	ft_is_ext_no_quotes(int c)
{
	return ((c >= 32 && c <= 255) && c != 127 && c != 129 && c != 141
		&& c != 143 && c != 144 && c != 157 && c != 160
		&& c != 173 && c != '\'' && c != '"');
}
