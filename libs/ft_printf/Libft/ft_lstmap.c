#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_node;
	t_list	*node;

	node = NULL;
	while (lst)
	{
		new_node = ft_lstnew(f(lst->content));
		if (!new_node)
		{
			ft_lstclear(&node, del);
			return (0);
		}
		ft_lstadd_back(&node, new_node);
		lst = lst->next;
	}
	new_node = NULL;
	return (node);
}
